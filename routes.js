function createRouteCard(routeNo, route) {
  const card = document.createElement('div');
  card.className = 'route-card';

  const title = document.createElement('h2');
  title.textContent = `Route ${routeNo} - ${route.name}`;
  card.appendChild(title);

  const table = document.createElement('table');
  const thead = table.createTHead();
  const headerRow = thead.insertRow();
  ['S.No', 'Place', 'Time'].forEach(text => {
    const th = document.createElement('th');
    th.textContent = text;
    headerRow.appendChild(th);
  });

  const tbody = document.createElement('tbody');
  route.places.forEach(([sno, place, time]) => {
    const row = tbody.insertRow();
    [sno, place, time].forEach(text => {
      const td = row.insertCell();
      td.textContent = text;
    });
  });
  table.appendChild(tbody);
  card.appendChild(table);

  const meta = document.createElement('div');
  meta.className = 'route-meta';
  meta.innerHTML = `🚍 Driver: ${route.driver}<br>👩‍🏫 Faculty In-charge: ${route.faculty}`;
  card.appendChild(meta);

  return card;
}

function showRoute(routeNo) {
  const container = document.getElementById('route-details');
  container.innerHTML = '';
  container.appendChild(createRouteCard(routeNo, routes[routeNo]));
}

window.onload = () => {
  const btnContainer = document.getElementById('route-buttons');
  for (let routeNo in routes) {
    const btn = document.createElement('button');
    btn.textContent = `Route ${routeNo}`;
    btn.onclick = () => showRoute(routeNo);
    btnContainer.appendChild(btn);
  }
  showRoute(Object.keys(routes)[0]);
};
